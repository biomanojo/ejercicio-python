def sumatoria():

    lista=[1,2,3,4,]
    suma =0
    for i in lista:
        suma =suma+i
        print("la suma es:", suma)
sumatoria()

def multipicacion():

    lista=[1,2,3,4,]
    multi=1
    for i in lista:
        multi =multi * i
        print("la multiplicacion es :",multi)
multipicacion()